//Question = calculating simple interest(SI)
//Formula for calculating interest = PRT/100
//principal(P)=100 , rate(R)=10 , time(T)=2

#include<stdio.h>
#include<conio.h>
int main ()
{
    float P,R,T,SI;              // 4 Container gets created (4 dabbe bana dea eik hi float se)
    P=100;
    R=10;
    T=2;
    SI=(P*R*T)/100;
    printf("Interest=%f",SI);
    return 0;
}




























