#include<stdio.h>
#include<conio.h>
int main ()
{
    char ch1 = 'A';       // value of capital A = 65
    char ch2 = 'a';        // ASCII value of small a = 97
    char ch3 = '#';        // ASCII value of special characters depends on them. 
    printf(" %d\n %d\n %d\n", ch1, ch2, ch3 );  // For finding ASCII values we write %d instead of %c
    return 0;

}
